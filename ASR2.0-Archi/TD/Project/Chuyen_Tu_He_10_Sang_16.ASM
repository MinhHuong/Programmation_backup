	CLO
	MOV	BL,0
	MOV	[A1],BL
	MOV	AL,B0
	MOV	[A2],AL

NHAP:
	IN	00
	CMP	AL,0D
	JZ	TRANSFOR
	SUB	AL,30
	PUSH	AL
	INC	BL
	MOV	[A0],BL		
	JMP	NHAP


TRANSFOR:
	MOV	BL,[A0]
	SUB	BL,4
	MOV	[A0],BL
	MOV	BL,0
	MOV	CL,0
	MOV	DL,1

BCDto16:
	CMP 	BL,4
	JZ 	SUITE
	POP 	AL
	MUL 	AL,DL
	ADD 	CL,AL
	MUL 	DL,2
	INC 	BL
	JMP 	BCDto16

SUITE:	
	MOV	AL,[A2]
	MOV	[AL],CL
	INC	AL
	MOV	[A2],AL	
	MOV	DL,[A1]
	ADD	DL,1
	MOV	[A1],DL	
	MOV	BL,[A0]
	CMP	BL,0
	JZ	TINH_RAM
	JNZ	TRANSFOR



TINH_RAM: 
	MOV	BL,C0	
	MOV	AL,[A2]
	DEC	AL

BEFORE_PRINT:
	CMP	DL,0
	JZ	QUIT
	JNZ	PRINT

PRINT:
	MOV	CL,[AL]	
	CMP 	CL,0
	JS 	ATOF

	CMP 	CL,A
	JS 	_0TO1
	JNS 	ATOF

_0TO1:
	ADD 	CL,30
	MOV 	[BL],CL
	DEC	DL	
	INC	BL
	DEC	AL
	JMP	BEFORE_PRINT

ATOF:
	ADD	CL,37
	MOV	[BL],CL
	DEC	DL
	INC	BL
	DEC	AL
	JMP	BEFORE_PRINT

QUIT: 

END




